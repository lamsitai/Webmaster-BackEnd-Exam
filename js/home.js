function blogHover1() {
    var a = document.getElementById('blog-hidden-1');
    var x = document.getElementById('blog-text-1');
    a.style.opacity = '1';
    x.style.marginTop = '20%';
}
function blogNormal1() {
    var a = document.getElementById('blog-hidden-1');
    var x = document.getElementById('blog-text-1');
    a.style.opacity = '0';
    x.style.marginTop = '0';
}
